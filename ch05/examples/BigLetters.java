package examples;

public class BigLetters 
{
    public static void main(String[] args) 
	{
	System.out.println("M         M         C C        PPPPPPPPP");
	System.out.println("M M     M M       C     C      P       P");
	System.out.println("M  M   M  M      C             P       P");
	System.out.println("M   M M   M      C             PPPPPPPPP");
	System.out.println("M    M    M      C             P        ");
	System.out.println("M         M       C     C      P        ");
	System.out.println("M         M         C C        P        ");
	}
}